package io.github.interestinglab.waterdrop;

/**
 */
public class WaterdropRuntimeException extends RuntimeException {

    public WaterdropRuntimeException() {
        super();
    }

    public WaterdropRuntimeException(String message) {
        super(message);
    }

    public WaterdropRuntimeException(String message, Throwable cause) {
        super(message, cause);
    }

    public WaterdropRuntimeException(Throwable cause) {
        super(cause);
    }
}
